                                    <br>
                                    <h2> What is Term Insurance?</h2>
                                    <p>Term insurance is a type of life insurance that provides coverage for a specific
                                        term or period. If the insured person dies during this term, the policy pays out
                                        a death benefit to the beneficiaries. If the insured survives the term, there is
                                        typically no payout, and the policy expires without any value.
                                    </p>
                                </div>
                                <h5>Key Features:</h5>
                                <ul>
                                    <li class="list-inline"><img src="assets/images/about/about-icon.svg"
                                            alt="about-icon">
                                        Coverage Period:</li>
                                    <p>o The policy is valid for a predetermined period, such as 10, 20, or 30 years.
                                        You can choose the term based on your financial goals and requirements.</p>
                                    <li class="list-inline"><img src="assets/images/about/about-icon.svg"
                                            alt="about-icon">Premiums:
                                    </li>
                                    <p> o Term insurance usually has lower premiums compared to other life insurance
                                        products like whole life or endowment policies. The premium amount is determined
                                        based on factors like age, health, sum assured, and term length.</p>
                                    <li class="list-inline"><img src="assets/images/about/about-icon.svg"
                                            alt="about-icon">Sum Assured:
                                    </li>
                                    <p>o This is the amount that will be paid to the beneficiaries in the event of the
                                        insured's death during the policy term. It can be chosen based on your financial
                                        needs and obligations.</p>
                                    <li class="list-inline"><img src="assets/images/about/about-icon.svg"
                                            alt="about-icon">No Survival Benefit:
                                    </li>
                                    <p>o Unlike some other life insurance policies, term insurance does not offer
                                        survival benefits. If the insured survives the policy term, there is no payout
                                        or return of premiums unless you opt for a specific plan with a return of
                                        premium feature.</p>
                                    <li class="list-inline"><img src="assets/images/about/about-icon.svg"
                                            alt="about-icon">Flexibility:
                                    </li>
                                    <p>o Policies can be customized to include riders (additional benefits) such as
                                        critical illness cover, accidental death benefit, or waiver of premium in case
                                        of disability.</p>
                                </ul>
